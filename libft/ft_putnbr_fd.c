#include "libft.h"

void		ft_putnbr_fd(int n, int fd)
{
	int		k;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	k = n / 10;
	if (k)
		ft_putnbr_fd(k, fd);
	ft_putchar_fd(n - (k * 10) + '0', fd);
}
