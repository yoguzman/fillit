char		*ft_strcat(char *dest, const char *src)
{
	char	*d;
	char	*s;

	s = (char*)src;
	d = dest;
	while (*d)
		d++;
	while (*s)
		*d++ = *s++;
	*d = 0;
	return (dest);
}
